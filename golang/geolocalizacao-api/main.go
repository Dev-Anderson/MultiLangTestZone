	if err != nil {
		fmt.Println("Falha ao criar o arquivo de texto", err.Error())
	}
	defer file.Close()

	_, err = file.Write(data)
	if err != nil {
		fmt.Println("Erro ao escrever no arquivo", err)
		return
	}

	fmt.Println("Os dados foram gravados com sucesso")

